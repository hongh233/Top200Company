-- 4. Create the Company table reference to the Industry and City (8 columns)
DROP TABLE IF EXISTS Company;
CREATE table Company (
	company_name VARCHAR(50),
    company_rank INT,
    industry_name VARCHAR(100),
    global_presence VARCHAR(15)
		-- Ensure valid global_presence
		CHECK (global_presence IN ('Low','Moderate-Low','Moderate','Moderate-High','High')),
    founder_is_CEO BOOLEAN,
    headquarters_city VARCHAR(50),
    company_type VARCHAR(10) 
		-- Ensure valid company_type
		CHECK (company_type IN ('private','public')),
    number_of_employees INT 
		-- Ensure number_of_employees is greater than zero
		CHECK (number_of_employees > 0),
	PRIMARY KEY (company_name),
    FOREIGN KEY (industry_name) REFERENCES Industry(industry_name),
    FOREIGN KEY (headquarters_city) REFERENCES City(headquarters_city)
);

